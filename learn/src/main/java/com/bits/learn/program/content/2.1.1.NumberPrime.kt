 ***********************************************************************************/

fun main(){
    primeNumber(11)
}

fun primeNumber(number: Int) : Boolean {
    for (i in 2 until Math.sqrt(number.toDouble()).toInt()+1){
        if (number%i == 0){
            return false
        }
    }
    return true
}

